package fr.exception.aspect;

public class AlongException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public AlongException() {
		super();

	}

	public AlongException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public AlongException(String message, Throwable cause) {
		super(message, cause);

	}

	public AlongException(String message) {
		super(message);

	}

	public AlongException(Throwable cause) {
		super(cause);

	}

}
